import path from 'path';
import { ChildProcess, exec } from 'child_process';
import { app, BrowserWindow, shell, ipcMain } from 'electron';
import { autoUpdater } from 'electron-updater';
import log from 'electron-log';
import MenuBuilder from './menu';
import { resolveHtmlPath } from './util';

class AppUpdater {
  constructor() {
    log.transports.file.level = 'info';
    autoUpdater.logger = log;
    autoUpdater.checkForUpdatesAndNotify();
  }
}

let mainWindow: BrowserWindow | null = null;
let gpt: ChildProcess | null = null;

// ./main -m ./models/llama-7b/ggml-model-q4_0.gguf -c 512 -b 1024 -n 256 --keep 48 \
//     --repeat_penalty 1.0 --color -i \
//     -r "User:" -f prompts/chat-with-bob.txt

ipcMain.on('init-gpt', () => {
  gpt = exec(
    `${process.cwd()}/assets/llamafile/mistral-7b-instruct-v0.1-Q4_K_M-main.llamafile ${[
      '-n',
      '256',
      '--repeat_penalty',
      '1.0',
      '-i',
      '-ins',
      '-r',
      '" >"',
    ].join(' ')}`,
  );

  gpt.stderr?.on('data', (data) => {
    if (data.toString('utf8').includes('== Running in interactive mode. ==')) {
      mainWindow?.webContents.send('gpt-initialized');
    }
  });
});

ipcMain.on('send-message-to-gpt', (event, arg) => {
  if (!gpt?.stdout || !gpt?.stdin) {
    return;
  }
  gpt.stdout.removeAllListeners('data');

  gpt.stdout.on('data', (data) => {
    const output = data.toString('utf8');
    event?.reply('chat-stream', output);
  });

  gpt.stdin.write(`${arg}\n`);
});

